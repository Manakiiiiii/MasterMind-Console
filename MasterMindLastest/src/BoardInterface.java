
/**
 * BoardInterface interface which will be implemented by Board class 
 * @author 池田愛樹
 *
 */
public interface BoardInterface {
	
	
	
	/**
	 * Prints the rule of the game
	 */
	public void printRules();
	/**
	 * Print the win message when player wins
	 */
	public void printWin();
	/**
	 * Print the lose message when player loses
	 */
	public void printLose();
	/**
	 * Print the message ask for user input
	 */
	public void printAskInput();
	
	
}
	


